#!/usr/bin/env node
/**
 * scripts/sync-asana-sprints.mjs
 *
 * Reusable snapshot script: fetches all "Glev — Sprint X:" projects from Asana
 * and writes a structured snapshot to the repo so the agent can quickly see the
 * current sprint state without writing ad-hoc API calls.
 *
 * Setup:
 *   1. Create a Personal Access Token at https://app.asana.com/0/my-apps
 *   2. Store it as ASANA_PAT in Replit Secrets (Vercel/Production does NOT need it).
 *
 * Usage:
 *   pnpm asana:sync                    # only open tickets (default)
 *   pnpm asana:sync -- --include-completed   # include completed tickets too
 *
 * Output:
 *   docs/asana/sprints.json  (raw snapshot)
 *   docs/asana/sprints.md    (human-readable, grouped by sprint)
 */

import { mkdir, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const ROOT = path.resolve(__dirname, '..');
const OUT_DIR = path.join(ROOT, 'docs', 'asana');
const OUT_JSON = path.join(OUT_DIR, 'sprints.json');
const OUT_MD = path.join(OUT_DIR, 'sprints.md');

const WORKSPACE_GID = '1213452700618853';
const SPRINT_NAME_RE = /^Glev — Sprint \d+:/;

const ASANA_BASE = 'https://app.asana.com/api/1.0';

function fail(msg) {
  console.error(`\x1b[31m✖ ${msg}\x1b[0m`);
  process.exit(1);
}
function info(msg) {
  console.log(`\x1b[36mℹ ${msg}\x1b[0m`);
}
function ok(msg) {
  console.log(`\x1b[32m✓ ${msg}\x1b[0m`);
}

const args = process.argv.slice(2);
const INCLUDE_COMPLETED = args.includes('--include-completed');

const TOKEN = process.env.ASANA_PAT;
if (!TOKEN) {
  fail(
    'ASANA_PAT is not set. Create a Personal Access Token at https://app.asana.com/0/my-apps and add it to Replit Secrets as ASANA_PAT.',
  );
}

async function asanaGet(pathname, params = {}) {
  const url = new URL(`${ASANA_BASE}${pathname}`);
  for (const [k, v] of Object.entries(params)) {
    if (v !== undefined && v !== null) url.searchParams.set(k, String(v));
  }
  const res = await fetch(url, {
    headers: {
      Authorization: `Bearer ${TOKEN}`,
      Accept: 'application/json',
    },
  });
  if (!res.ok) {
    const body = await res.text().catch(() => '');
    fail(`Asana API ${res.status} ${res.statusText} for ${url.pathname}\n${body}`);
  }
  return res.json();
}

async function listSprintProjects() {
  info('Listing projects in workspace…');
  const projects = [];
  let offset;
  do {
    const data = await asanaGet(`/workspaces/${WORKSPACE_GID}/projects`, {
      opt_fields: 'name',
      limit: 100,
      offset,
    });
    for (const p of data.data || []) {
      if (SPRINT_NAME_RE.test(p.name)) projects.push(p);
    }
    offset = data.next_page?.offset;
  } while (offset);
  projects.sort((a, b) => {
    const na = Number(a.name.match(/Sprint (\d+)/)?.[1] || 0);
    const nb = Number(b.name.match(/Sprint (\d+)/)?.[1] || 0);
    return na - nb;
  });
  ok(`Found ${projects.length} sprint projects`);
  return projects;
}

async function fetchProjectTasks(project) {
  const params = {
    opt_fields:
      'name,completed,due_on,notes,assignee.name,memberships.section.name',
    limit: 100,
    // Asana's task-list endpoints default to open-only when `completed_since`
    // is omitted. To reliably include completed tasks too, pass an epoch-old
    // timestamp; to keep open-only, pass 'now'.
    completed_since: INCLUDE_COMPLETED ? '1970-01-01T00:00:00Z' : 'now',
  };

  const tasks = [];
  let offset;
  do {
    const data = await asanaGet(`/projects/${project.gid}/tasks`, {
      ...params,
      offset,
    });
    for (const t of data.data || []) {
      if (!INCLUDE_COMPLETED && t.completed) continue;
      const section = t.memberships?.[0]?.section?.name ?? null;
      tasks.push({
        gid: t.gid,
        name: t.name,
        completed: !!t.completed,
        due_on: t.due_on ?? null,
        assignee: t.assignee ? { gid: t.assignee.gid, name: t.assignee.name } : null,
        section,
        notes: t.notes ?? '',
      });
    }
    offset = data.next_page?.offset;
  } while (offset);
  return tasks;
}

function todayIsoDate() {
  return new Date().toISOString().slice(0, 10);
}

function renderMarkdown(snapshot) {
  const today = todayIsoDate();
  const lines = [];
  lines.push('# Asana Sprint Snapshot');
  lines.push('');
  lines.push(`_Generated: ${new Date().toISOString()}_`);
  lines.push(
    `_Filter: ${INCLUDE_COMPLETED ? 'open + completed' : 'open only'}_`,
  );
  lines.push('');
  lines.push(
    'Auto-generated by `scripts/sync-asana-sprints.mjs`. Re-run with `pnpm asana:sync` to refresh.',
  );
  lines.push('');

  for (const sprint of snapshot.sprints) {
    const total = sprint.tasks.length;
    const open = sprint.tasks.filter((t) => !t.completed).length;
    const done = total - open;
    const overdue = sprint.tasks.filter(
      (t) => !t.completed && t.due_on && t.due_on < today,
    ).length;

    lines.push(`## ${sprint.name}`);
    lines.push('');
    lines.push(
      `**${total} tasks** — ${open} open, ${done} done${overdue ? `, **${overdue} overdue**` : ''}`,
    );
    lines.push('');
    if (total === 0) {
      lines.push('_No tasks._');
      lines.push('');
      continue;
    }
    lines.push('| Status | Due | Assignee | Task |');
    lines.push('| --- | --- | --- | --- |');
    const sorted = [...sprint.tasks].sort((a, b) => {
      const da = a.due_on || '9999-99-99';
      const db = b.due_on || '9999-99-99';
      return da.localeCompare(db);
    });
    for (const t of sorted) {
      const status = t.completed ? '✓' : '○';
      let due = t.due_on || '—';
      if (!t.completed && t.due_on && t.due_on < today) due = `⚠️ ${t.due_on}`;
      const assignee = t.assignee?.name || '—';
      const name = t.name.replace(/\|/g, '\\|');
      lines.push(`| ${status} | ${due} | ${assignee} | ${name} |`);
    }
    lines.push('');
  }
  return lines.join('\n');
}

async function main() {
  const projects = await listSprintProjects();
  if (projects.length === 0) {
    fail('No "Glev — Sprint X:" projects found in workspace.');
  }

  info('Fetching tasks for each sprint in parallel…');
  const results = await Promise.all(
    projects.map(async (p) => {
      const tasks = await fetchProjectTasks(p);
      const open = tasks.filter((t) => !t.completed).length;
      const done = tasks.length - open;
      ok(`  ${p.name}: ${tasks.length} tasks (${open} open, ${done} done)`);
      return {
        gid: p.gid,
        name: p.name,
        tasks,
      };
    }),
  );

  const snapshot = {
    generated_at: new Date().toISOString(),
    workspace_gid: WORKSPACE_GID,
    include_completed: INCLUDE_COMPLETED,
    sprints: results,
  };

  await mkdir(OUT_DIR, { recursive: true });
  await writeFile(OUT_JSON, JSON.stringify(snapshot, null, 2) + '\n', 'utf8');
  ok(`Wrote ${path.relative(ROOT, OUT_JSON)}`);

  const md = renderMarkdown(snapshot);
  await writeFile(OUT_MD, md, 'utf8');
  ok(`Wrote ${path.relative(ROOT, OUT_MD)}`);
}

main().catch((err) => {
  fail(err?.stack || err?.message || String(err));
});
